class RelationSchema:
    """关系 Schema 与归一化器

    两层归一化：
    1. L1 正则模式：处理带年份/编号的变体（"2018年亩产" → "产量"）
    2. L2 同义词字典：处理近义关系（"症状" → "症状表现"）

    用法::

        schema = RelationSchema()
        schema.normalize_relation("2018年平均亩产")  # → "产量"
        schema.normalize_relation("典型症状")        # → "症状表现"
        schema.normalize_relation("未知关系")        # → "未知关系"（保留原样）
    """

    def __init__(
        self,
        allowed_relations: Optional[List[str]] = None,
        synonyms: Optional[Dict[str, str]] = None,
        patterns: Optional[List[Tuple[str, str]]] = None,
        schema_path: Optional[str] = None,
        entity_types: Optional[List[str]] = None,
    ):
        """初始化 schema

        Args:
            allowed_relations: 标准关系白名单。None 用默认
            synonyms: 同义词归一化字典 {原关系: 标准关系}。None 用默认
            patterns: 正则模式归一化 [(pattern, canonical), ...]。None 用默认
            schema_path: 自定义 schema JSON 文件路径，覆盖上述默认值
        """
        # 默认值
        self.allowed_relations = list(DEFAULT_ALLOWED_RELATIONS)
        self.synonyms = dict(DEFAULT_SYNONYMS)
        self.patterns = list(DEFAULT_PATTERNS)
        self.entity_types = list(DEFAULT_ENTITY_TYPES)

        # 从 JSON 文件加载自定义 schema（覆盖默认）
        if schema_path and os.path.exists(schema_path):
            self._load_from_json(schema_path)
            logger.info("已加载自定义 schema: %s", schema_path)
        else:
            # 应用传入参数覆盖
            if allowed_relations is not None:
                self.allowed_relations = list(allowed_relations)
            if synonyms is not None:
                self.synonyms = dict(synonyms)
            if patterns is not None:
                self.patterns = list(patterns)
            if entity_types is not None:
                self.entity_types = list(entity_types)

        # 预编译正则
        self._compiled_patterns = [
            (re.compile(p), canonical) for p, canonical in self.patterns
        ]

        # 归一化统计
        self._stats = {"normalized": 0, "unchanged": 0, "unknown": 0}

    def _load_from_json(self, path: str) -> None:
        """从 JSON 文件加载 schema

        JSON 格式::

            {
                "allowed_relations": ["症状表现", "防治", ...],
                "synonyms": {"症状": "症状表现", ...},
                "patterns": [["\\d{4}年.*亩产", "产量"], ...]
            }
        """
        try:
            with open(path, encoding="utf-8") as f:
                data = json.load(f)
            if "entity_types" in data:
                self.entity_types = list(data["entity_types"])
            if "allowed_relations" in data:
                self.allowed_relations = list(data["allowed_relations"])
            if "synonyms" in data:
                self.synonyms = dict(data["synonyms"])
            if "patterns" in data:
                # JSON 里是 [[pattern, canonical], ...]，转成 list of tuple
                self.patterns = [tuple(item) for item in data["patterns"]]
        except Exception as e:
            logger.warning("加载 schema JSON 失败 %s: %s，使用默认 schema", path, e)

    def normalize_relation(self, relation: str) -> str:
        """归一化关系名

        三层归一化（按优先级）：
        1. 同义词字典精确匹配："症状" → "症状表现"
        2. 白名单直接通过：已在标准关系列表中的不经过正则（避免"防治"被误归为"防治方法"）
        3. 正则模式匹配：处理带年份/编号的变体 "2018年亩产" → "产量"

        Args:
            relation: 原始关系名

        Returns:
            归一化后的标准关系名（未匹配则返回原样）
        """
        if not relation:
            return relation

        rel = relation.strip()

        # L1: 同义词字典精确匹配
        if rel in self.synonyms:
            self._stats["normalized"] += 1
            return self.synonyms[rel]

        # L2: 白名单直接通过（避免正则把标准关系误归一化）
        if rel in self.allowed_relations:
            self._stats["unchanged"] += 1
            return rel

        # L3: 正则模式匹配（只处理不在白名单中的关系）
        for pattern, canonical in self._compiled_patterns:
            if pattern.search(rel):
                self._stats["normalized"] += 1
                return canonical

        # 未匹配
        self._stats["unknown"] += 1
        return rel

    def is_allowed(self, relation: str) -> bool:
        """检查关系是否在白名单中"""
        return relation.strip() in self.allowed_relations

    def get_canonical_relations(self) -> List[str]:
        """获取标准关系白名单"""
        return list(self.allowed_relations)

    def build_prompt_constraint(self) -> str:
        """构建注入 LLM 抽取 prompt 的 schema 约束文本

        v0.3.1: 强化为硬约束，明确要求 LLM 只使用白名单关系名，
        减少 LLM 自由发散产生的新碎片化关系。

        Returns:
            prompt 片段，列出允许的标准关系名
        """
        rels = "、".join(self.allowed_relations)
        types = "、".join(self.entity_types) if self.entity_types else ""
        entity_hint = (
            f"\n## 实体类型提示（软约束）\n"
            f"head/tail 实体应尽量属于以下领域类型：\n"
            f"{types}\n"
            f"实体名必须是简洁的名词短语（2-15 字），禁止句子片段、用法描述或带数字的步骤说明。\n"
            f"错误示例：'80%乙蒜素2000倍液浸种48小时'、'每亩用药量100克'（应为 '乙蒜素'、'用量说明'）\n"
        ) if types else ""
        return (
            f"\n## 关系 Schema 约束（硬约束）\n"
            f"抽取三元组时，relation 字段**必须**从以下标准关系名中选择：\n"
            f"{rels}\n\n"
            f"只有当文本信息确实无法映射到上述任何关系时，才可使用自定义关系名，"
            f"且必须简洁规范（2-6 个汉字，避免句子片段）。\n"
            f"错误示例：'2018年亩产'、'最佳防治时期'、'每亩使用量'（应分别用 '产量'、'防治方法'、'用法用量'）\n"
            f"{entity_hint}"
        )

    def get_stats(self) -> dict:
        """获取归一化统计"""
        return dict(self._stats)

    def reset_stats(self) -> None:
        """重置统计"""
        self._stats = {"normalized": 0, "unchanged": 0, "unknown": 0}
